function mobileMenu() {
	let helen_mobile_menu = document.querySelector(".helen_menu");
	let helen_mobile_bars = document.querySelector(".bars");
	let helen_mobile_close = document.querySelector(".closeMob");
	helen_mobile_menu.classList.toggle("block");
	helen_mobile_bars.classList.toggle("visible");
	helen_mobile_close.classList.toggle("visible");
}
